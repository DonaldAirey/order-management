namespace Teraque.Windows.Controls
{

	/// <summary>
	/// The types of ColumnViewColumnHeaders.
	/// </summary>
	public enum ColumnViewColumnHeaderRole
	{

		/// <summary>
		/// The column that is being dragged to a new location in the header.
		/// </summary>
		Dragging,

		/// <summary>
		/// A normal column.
		/// </summary>
		Normal,

		/// <summary>
		/// The column that fills the space between the right-most column and the edge of the viewport.
		/// </summary>
		Padding

	}

}
